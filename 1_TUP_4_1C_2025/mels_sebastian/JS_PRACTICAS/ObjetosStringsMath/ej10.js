/* Calcula y muestra en consola:
● La raíz cuadrada de 64
● 5 elevado a la 3
● El valor absoluto de -15
● El número mayor entre 10, 5 y 20*/

let raizCuadrada = Math.sqrt(64); 
console.log("Raíz cuadrada de 64:", raizCuadrada); 

let potencia = Math.pow(5, 3); 
console.log("5 elevado a la 3:", potencia); 

let valorAbsoluto = Math.abs(-15); 
console.log("Valor absoluto de -15:", valorAbsoluto); 

let maximo = Math.max(10, 5, 20); 
console.log("Número mayor entre 10, 5 y 20:", maximo); 

